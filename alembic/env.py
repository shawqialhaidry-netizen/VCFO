"""
alembic/env.py
--------------
Alembic migration environment for VCFO.

Connects to the same database and models as the application so that
`alembic revision --autogenerate` automatically detects model changes.

Usage:
    alembic upgrade head              # apply all pending migrations
    alembic revision --autogenerate -m "add X column"  # generate new migration
"""
import os
import sys
from logging.config import fileConfig

from sqlalchemy import engine_from_config, pool
from alembic import context

# ?????? Make sure the project root is on sys.path ???????????????????????????????????????????????????????????????????????????????????????????????????
# This allows `from app.xxx import yyy` to work from the alembic/ directory.
sys.path.insert(0, os.path.abspath(os.path.join(os.path.dirname(__file__), "..")))

# ?????? Import app config and all models ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
# Models must be imported so that Base.metadata knows about all tables.
from app.core.config import settings                          # noqa: E402
from app.core.database import Base                            # noqa: E402
from app.models import company, trial_balance, branch, user, membership  # noqa: E402, F401

# ?????? Alembic Config object ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
config = context.config

# Set the database URL from app settings (respects .env file)
config.set_main_option("sqlalchemy.url", settings.DATABASE_URL)

# Interpret the config file for Python logging
if config.config_file_name is not None:
    fileConfig(config.config_file_name)

# target_metadata enables autogenerate support
target_metadata = Base.metadata


# ?????? Offline mode (generate SQL without connecting) ????????????????????????????????????????????????????????????????????????????????????
def run_migrations_offline() -> None:
    """Run migrations in 'offline' mode - generates SQL script without a live DB."""
    url = config.get_main_option("sqlalchemy.url")
    context.configure(
        url=url,
        target_metadata=target_metadata,
        literal_binds=True,
        dialect_opts={"paramstyle": "named"},
        # Compare server defaults so autogenerate detects DEFAULT changes
        compare_server_default=True,
        # Render AS BATCH for SQLite (required for ALTER TABLE support)
        render_as_batch=True,
    )
    with context.begin_transaction():
        context.run_migrations()


# ?????? Online mode (connect and apply migrations directly) ?????????????????????????????????????????????????????????????????????
def run_migrations_online() -> None:
    """Run migrations in 'online' mode - connects to DB and applies changes."""
    connectable = engine_from_config(
        config.get_section(config.config_ini_section, {}),
        prefix="sqlalchemy.",
        poolclass=pool.NullPool,
    )
    with connectable.connect() as connection:
        context.configure(
            connection=connection,
            target_metadata=target_metadata,
            # Compare server defaults so autogenerate detects DEFAULT changes
            compare_server_default=True,
            # render_as_batch is REQUIRED for SQLite ALTER TABLE support
            # SQLite does not support ADD COLUMN with constraints natively;
            # batch mode creates a new table, copies data, and drops the old one.
            render_as_batch=True,
        )
        with context.begin_transaction():
            context.run_migrations()


if context.is_offline_mode():
    run_migrations_offline()
else:
    run_migrations_online()
